export function convertToUnicode(text) {
  const emojiRegex = /(:\S*:)/g;

  let match;
  while ((match = emojiRegex.exec(text)) != null) {
    if (emojiList[match[0]]) {
      text = text.replace(match[0], emojiList[match[0]]);
    }
  }

  return text;
}

export default helper(convertToUnicode);

